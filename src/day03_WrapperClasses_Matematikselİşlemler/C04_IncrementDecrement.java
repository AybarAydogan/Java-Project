package day03_WrapperClasses_Matematikselİşlemler;

public class C04_IncrementDecrement {

    public static void main(String[] args) {

        int sayi = 20 ;

        sayi = sayi + 5 ;

        System.out.println(sayi); // 25

        // sayiyi 3 artırıp yazdırın

        System.out.println(sayi += 3); // 28

        System.out.println(sayi); // 28

        // sayının değerini 2 katına çıkarıp yazdırın

        System.out.println(sayi *= 2 ); // 56

    }
}
